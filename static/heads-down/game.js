class Orientation {
  last = ORIENTATION_NONE;
  subscribers = [];
  lastEventTime = 0;

  constructor() {
    window.addEventListener("deviceorientation", this.handleOrientation, true);
  }

  handleOrientation = (evt) => {
    let current = eventToOrientation(evt);
    const now = performance.now();
    if (now - this.lastEventTime < ORIENTATION_DEBOUNCE_MS) {
      return;
    }

    if (this.last === ORIENTATION_MIDDLE && (
      current === ORIENTATION_DOWN || current === ORIENTATION_UP)) {
      this.lastEventTime = now;

      for (let subscriber of this.subscribers) {
        subscriber(current);
      }
    }

    this.last = current;
  }


  /**
   * The function will be passed ORIENTATION_DOWN or ORIENTATION_UP whenever the device is rotated down or up
   */
  subscribe(f) {
    this.subscribers.push(f);
  }

  unsubscribe(f) {
    this.subscribers = this.subscribers.filter(sub => sub !== f);
  }
}

function isAtSillyAngle(beta, gamma) {
  // Extreme pitch
  if (Math.abs(gamma) < 45) {
    return true;
  }
  // Extreme roll
  if (gamma < 0) {
    if (Math.abs(beta) > SILLY_ROLL) {
      return true;
    }
  } else {
    if (Math.abs(beta) < 180 - SILLY_ROLL) {
      return true;
    }
  }
  return false;
}

function eventToOrientation(evt) {
  const beta = evt.beta;
  const gamma = evt.gamma;
  if (DEBUG_ORIENTATION) {
    debug.innerText = `${Math.round(evt.beta)}\n${Math.round(evt.gamma)}`;
  }
  if (isAtSillyAngle(beta, gamma)) {
    return ORIENTATION_NONE;
  }
  if (gamma > DOWN_THRESHOLD || gamma < UP_THRESHOLD) {
    return ORIENTATION_MIDDLE;
  }
  if (gamma < 0) {
    return ORIENTATION_UP;
  }
  return ORIENTATION_DOWN;
}

