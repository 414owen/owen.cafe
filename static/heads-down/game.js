const shuffle = arr => arr
  .map(a => ({r: Math.random(), item: a}))
  .sort((a, b) => a.r - b.r)
  .map(a => a.item);

const displayWord = (word, color="") => {
  mainEl.style.backgroundColor = color;
  if (typeof(word) === "object") {
    wordDiv.innerText = word.phrase;
    wordNoteDiv.innerText = `(${word.note})`;
  } else {
    wordDiv.innerText = word;
    wordNoteDiv.innerText = "";
  }
};

const playFromStart = (audio) => {
  audio.currentTime = 0;
  audio.play();
};

const sleep = (ms) => new Promise(resolve => setTimeout(resolve, ms));

const S_COUNTDOWN = 0;
const S_WORD = 1;
const S_AFTER_TILT = 2;
const S_END = 3;

const onClickCategory = category => async () => {
  requestFullscreen(document.body);
  screen.orientation.lock("landscape");

  const words = shuffle(category.words);
  let state = S_COUNTDOWN;
  let i = 0;
  let numDisplayed = 0;
  let incorrect = [];

  const sub = async direction => {
    if (direction === ORIENTATION_UP) {
      incorrect.push(words[i]);
      state = S_AFTER_TILT;
      displayWord(words[i], "red");
      playFromStart(incorrectMp3);
      await sleep(500);
    } else {
      state = S_AFTER_TILT;
      displayWord(words[i], "green");
      playFromStart(correctMp3);
      await sleep(500);
    }
    i += 1;
    if (i === words.length) {
      endGame();
    } else {
      state = S_WORD;
      numDisplayed++;
      displayWord(words[i]);
    }
  };

  const displayResults = () => {
    mainEl.className = "results";
    scoreDiv.innerText = `${numDisplayed - incorrect.length}/${numDisplayed}`;
    missedWordsList.innerHTML = "";
    for (const word of incorrect) {
      const node = document.createElement("li");
      node.innerText = typeof(word) === "object"
        ? `${word.phrase} (${word.note})`
        : word;
      missedWordsList.appendChild(node);
    }
  };

  displayWord(3);
  mainEl.className = "round";
  for (let i = 3; i >= 1; i--) {
    displayWord(i);
    await sleep(1000);
  }
  state = S_WORD;
  numDisplayed++;
  displayWord(words[i]);

  const clickHandler = (ev) => {
    // Click left side of page for correct answers
    const correct = ev.pageX < 0.5 * window.innerWidth;
    sub(correct ? ORIENTATION_DOWN : ORIENTATION_UP);
  };
  mainEl.addEventListener("click", clickHandler);
  orientation.subscribe(sub);

  const endGame = () => {
    state = S_END;
    playFromStart(gongMp3);
    cleanup();
    displayResults();
  };

  let seconds = ROUND_TIME;

  const displayCount = () => {
    timerDiv.innerText = seconds;
  };

  displayCount();

  const countDown = () => {
    seconds--;
    if (seconds === 5) {
      playFromStart(tickingMp3);
    }
    if (seconds === 0) {
      if (state == S_WORD) {
        incorrect.push(words[i]);
      }
      endGame();
      return;
    }
    displayCount();
  };

  const timerId = setInterval(countDown, 1000);

  const cleanup = () => {
    mainEl.removeEventListener("click", clickHandler);
    orientation.unsubscribe(sub);
    screen.orientation.unlock();
    clearInterval(timerId);
    displayWord("");
  };
};

